package ISSTask1;

//Point 3: Loop control and decision making
public class LoopExample {
 public static void main(String[] args) {
     // Loop control
     for (int i = 1; i <= 5; i++) {
         // Decision making
         if (i % 2 == 0) {
             System.out.println(i + " is even");
         } else {
             System.out.println(i + " is odd");
         }
     }
 }
}
